package com.ddp.device.document;

/**
 * 위험도 등급
 * 로그 분석 결과에 따른 위험도 분류
 */
public enum RiskLevel {
    /**
     * 긴급 - 즉시 조치 필요
     * - 조작 시도 3회 이상
     * - 장치 탈거 감지
     * - 로그 미제출 3회 이상
     */
    HIGH,
    
    /**
     * 경고 - 주의 관찰
     * - 과도한 실패율
     * - 연속 실패 5회 이상
     * - 데이터 불일치
     */
    MEDIUM,
    
    /**
     * 정상 - 모니터링 지속
     * - 정상 범위 내
     */
    LOW
}
